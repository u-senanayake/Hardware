    private void discountedAmount(){
          try{
              DBHelp db=new DBHelp();
                    String id=TXT_ID_NUMBER.getText();
                    double max_discount=db.max_discount(new Item(id)),
                            discount=Double.valueOf(TXT_ITEM_DISC.getText());
                    if(max_discount>=discount){
                        double unit_price=db.sell_price(new Item(id)),
                                qty=Double.valueOf(TXT_QTY.getText());
                        double amount=unit_price*qty;
                        double x=100-discount;
                        double final_amount=(amount*x)/100;
                        TXT_AMOUNT.setText(String.valueOf(final_amount));
                        addDataToTemp();
                    }
          }catch(Exception e){
              System.out.println(e.getMessage()+"Discounted amount");
          }
                    
    }         
