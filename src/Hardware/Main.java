    private void BTN_SEARCH_CUSTOMERActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_SEARCH_CUSTOMERActionPerformed
        double HAVE_PAY=0,DUE_AMOUNT=0;
        try{
            String cus_name=TXT_SEARCH_NAME.getText();
            String id;
            DBHelp db= new DBHelp();
            //get id for the search name
            id=db.getCustomerID(new Customer(cus_name));
            //get customer's details
            conn=DBConnect.Connect();
            
            String sql="select * from CUSTOMER where CUS_ID='"+id+"'";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next()){
                TXT_ID.setText(rs.getString("CUS_ID"));
                TXT_NAME.setText(rs.getString("CUS_NAME")); 
                TXT_ADRESS.setText(rs.getString("CUS_ADRESS"));
                TXT_MOBILE.setText(rs.getString("CUS_CONTACT_MOBILE"));
                TXT_OFFICE.setText(rs.getString("CUS_CONTACT_WORK"));
                TXT_CREDICT_LEVEL.setText(String.valueOf(rs.getDouble("CUS_CREDICT_LEVEL")));
         }
            String sql1="select CUS_INVOICE_NUMBER,BILL_DATE,PAY_AMOUNT,STATE from CUS_INVOICE where CUS_ID='"+id+"'";
            pst1=conn.prepareStatement(sql1);
            rs1=pst1.executeQuery();
            TBL_BILL.setModel(DbUtils.resultSetToTableModel(rs1));
            
            String sql3="select CHEQ_NUMBER,PAY_DATE,AMOUNT,METHOD,BANK from CREDICT_PAY where CUS_ID='"+id+"'";
            pst3=conn.prepareStatement(sql3);
            rs3=pst3.executeQuery();
            TBL_PAYMENTS.setModel(DbUtils.resultSetToTableModel(rs3));
            
            String sql2="select SUM(PAY_AMOUNT)AS total from CUS_INVOICE where CUS_ID='"+id+"' and STATE='credict'";
            pst2=conn.prepareStatement(sql2);
            rs2=pst2.executeQuery();
            if(rs2.next()){
               HAVE_PAY= rs2.getDouble(1);
                
            }
       }
       catch (Exception exp){
           System.out.println(exp.getMessage()+"Search Button");
       }
        DUE_AMOUNT=Double.valueOf(TXT_CREDICT_LEVEL.getText())-HAVE_PAY;
        TXT_HAVE_PAY.setText(String.valueOf(HAVE_PAY));
        TXT_DUE.setText(String.valueOf(DUE_AMOUNT));
        if(DUE_AMOUNT>=0){
            TXT_DUE.setBackground(Color.green);
        }
        if(DUE_AMOUNT<0){
            TXT_DUE.setBackground(Color.red);
        }
            
       
        
    }//GEN-LAST:event_BTN_SEARCH_CUSTOMERActionPerformed

