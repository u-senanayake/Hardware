      private void CalculationGRN(){
          DBHelpGRN db= new DBHelpGRN();
          String item_code=TXT_GRN_ITEM_CODE.getText();
          String discri=TXT_DISCR_2.getText();
          double qty=Double.valueOf(TXT_QTY_2.getText());
          double unit_price=Double.valueOf(TXT_UNIT_PRICE_2.getText());
          double cost=Double.valueOf(TXT_COSY_2.getText());
          double max_disc=Double.valueOf(TXT_MAX_DISC_2.getText());
          double amount=cost*qty;
         db.addTempGRN(new tempGRN(item_code, discri, qty, unit_price, cost, max_disc, amount));
         ResultSet rs=db.getTempAll();
         TBL_2.setModel(DbUtils.resultSetToTableModel(rs));
         //get total amount
         double total_amount=db.getTotalAmount();
         double bill_disc=Double.valueOf(TXT_BILL_DISC_2.getText());
         double final_amount=(total_amount*(100-bill_disc))/100;
         TXT_TOTAL.setText(String.valueOf(final_amount));
      }
