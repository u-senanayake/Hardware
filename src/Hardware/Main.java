      private void getDetailsFromCodeGRN(){
    
        
              //  e.consume();
                DBHelpGRN db=new DBHelpGRN();
                String id=TXT_GRN_ITEM_CODE.getText();
                //check the enter code is correct
                int count=db.getCodeErr(new tempGRN(id));
                if(count==1){
                    String dis=db.getDescription(new tempGRN(id));
                    double stock=db.getQTY(new tempGRN(id));
                    double unit_price=db.getUnitPrice(new tempGRN(id));
                    double cost=db.getCost(new tempGRN(id));
                    double max_disc=db.getMaxDisc(new tempGRN(id));
                TXT_DISCR_2.setText(dis);
                //TXT_QTY_2.setText(String.valueOf(stock));
                TXT_UNIT_PRICE_2.setText(String.valueOf(unit_price));
                TXT_COSY_2.setText(String.valueOf(cost));
                TXT_MAX_DISC_2.setText(String.valueOf(max_disc));
                }
                if(count==0){
                    JOptionPane.showMessageDialog(null ,"Code is error. Please try again");
                }
}
