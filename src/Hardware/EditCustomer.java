          String sql="update CUSTOMER set CUS_NAME ='"+_name+"',CUS_ADRESS='"+_adress+"',CUS_CONTACT_MOBILE='"+_mobile+"',CUS_CONTACT_WORK='"+_work+"',CUS_CREDICT_LEVEL='"+_credict+"'  where CUS_ID=?";
