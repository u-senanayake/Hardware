    public void EditCus(EdidCustomerHealp edidCustomerHealp){
        name=edidCustomerHealp.getName();
        
       try{
        DBHelp db= new DBHelp();
        id=db.getCustomerID(new Customer(name));
        conn=DBConnect.Connect();
        String sql="select * from CUSTOMER where CUS_ID='"+id+"'";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next()){
                TXT_ID.setText(rs.getString("CUS_ID"));
                TXT_NAME.setText(rs.getString("CUS_NAME")); 
                TXT_ADRESS.setText(rs.getString("CUS_ADRESS"));
                TXT_MOBILE.setText(rs.getString("CUS_CONTACT_MOBILE"));
                TXT_WORK.setText(rs.getString("CUS_CONTACT_WORK"));
                TXT_CREDICT_LEVEL.setText(String.valueOf(rs.getDouble("CUS_CREDICT_LEVEL")));
         }
       }
       catch(Exception exp){
           System.out.println(exp.getMessage());
       }
    }
